// 3 types of declaring a function

// 1. normal way of function 
function print(){
    console.log("hello");
  }
  print();
  
  //2. anonymous function.
  let print1= function (){
    console.log("hello");
  }
  print1();
  
  //3 arrow function.
  
  let print2= () =>{
    console.log("hello");
  }
  print2();